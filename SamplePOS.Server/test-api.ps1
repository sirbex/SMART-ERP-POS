# TEST 7: GET FEFO BATCHES FOR PRODUCT
# ============================================================
Write-Host "`n[TEST 7] Testing FEFO Batch Selection..." -ForegroundColor Yellow

try {
    $fefoResponse = Invoke-WebRequest `
        -Uri "$baseUrl/api/inventory/batches/fefo/${productId}?requestedQuantity=50" `
        -UseBasicParsing
    
    $fefo = ($fefoResponse.Content | ConvertFrom-Json).data
    
    Write-Host "   ✅ FEFO Batches Retrieved!" -ForegroundColor Green
    Write-Host "      Requested Quantity: 50" -ForegroundColor Cyan
    Write-Host "      Batches Available: $($fefo.batches.Count)" -ForegroundColor Cyan
    Write-Host "      Allocated Quantity: $($fefo.totalAllocated)" -ForegroundColor Cyan
    
    foreach ($batch in $fefo.batches) {
        Write-Host "      - Batch: $($batch.batchNumber)" -ForegroundColor Cyan
        Write-Host "        Expiry: $($batch.expiryDate)" -ForegroundColor Gray
        Write-Host "        Available: $($batch.remainingQuantity)" -ForegroundColor Gray
        Write-Host "        Allocated: $($batch.allocatedQuantity)" -ForegroundColor Gray
    }
    
    # Validation: Batches should be sorted by expiry date (earliest first)
    if ($fefo.batches.Count -gt 1) {
        for ($i = 0; $i -lt ($fefo.batches.Count - 1); $i++) {
            $currentExpiry = [DateTime]::Parse($fefo.batches[$i].expiryDate)
            $nextExpiry = [DateTime]::Parse($fefo.batches[$i + 1].expiryDate)
            if ($currentExpiry -gt $nextExpiry) {
                Write-Host "   ❌ ERROR: FEFO ordering incorrect - batch $i expires after batch $($i+1)" -ForegroundColor Red
            }
        }
    }
    
} catch {
    Write-Host "   ❌ TEST 7 FAILED: $($_.Exception.Message)" -ForegroundColor Red
}

